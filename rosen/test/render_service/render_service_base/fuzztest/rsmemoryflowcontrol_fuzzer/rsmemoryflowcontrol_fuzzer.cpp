#include "rsmemoryflowcontrol_fuzzer.h"

#include <securec.h>

#include "memory/rs_memory_flow_control.h"

namespace OHOS {
namespace Rosen {
namespace {
const uint8_t* g_data = nullptr;
size_t g_size = 0;
size_t g_pos;
} // namespace

template<class T>
T GetData()
{
    T object {};
    size_t objectSize = sizeof(object);
    if (g_data == nullptr || objectSize > g_size - g_pos) {
        return object;
    }
    errno_t ret = memcpy_s(&object, objectSize, g_data + g_pos, objectSize);
    if (ret != EOK) {
        return {};
    }
    g_pos += objectSize;
    return object;
}

bool MemoryFlowControlFuzzTest(const uint8_t* data, size_t size)
{
    if (data == nullptr) {
        return false;
    }

    // initialize
    g_data = data;
    g_size = size;
    g_pos = 0;

    MemoryFlowControl::GetAshmemFlowControlThreshold();
    auto& instance = MemoryFlowControl::Instance();
    instance.AddAshmemStatistic(GetData<pid_t>(), GetData<uint32_t>());
    instance.RemoveAshmemStatistic(GetData<pid_t>(), GetData<uint32_t>());
    return true;
}

bool AshmemFlowControlUnitFuzzTest(const uint8_t* data, size_t size)
{
    if (data == nullptr) {
        return false;
    }

    // initialize
    g_data = data;
    g_size = size;
    g_pos = 0;

    AshmemFlowControlUnit::CheckOverflowAndCreateInstance(GetData<pid_t>(), GetData<uint32_t>());
    return true;
}
} // namespace Rosen
} // namespace OHOS

/* Fuzzer entry point */
extern "C" int LLVMFuzzerTestOneInput(const uint8_t* data, size_t size)
{
    /* Run your code on data */
    OHOS::Rosen::MemoryFlowControlFuzzTest(data, size);
    OHOS::Rosen::AshmemFlowControlUnitFuzzTest(data, size);
    return 0;
}
